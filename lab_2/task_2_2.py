"""" Draw with library turtle
"""
import turtle

turtle.shape('turtle')      # cursor looks like turtle
turtle.speed(1)             # int 1..10

turtle.fd(50)               # Go 50 pixels forward
turtle.left(90)             # Turn left 90 degrees
turtle.fd(50)
turtle.left(90)
turtle.fd(50)
turtle.right(90)
turtle.fd(50)
turtle.right(90)
turtle.fd(50)

x = input()                 # wait until some input
